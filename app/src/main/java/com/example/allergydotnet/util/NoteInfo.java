public class NoteInfo {

    private int user_id;
    private String note_name;
    private String note_text;
    private String note_time;


    public int getUser_id() {
        return user_id;
    }

    public String getNote_name() {
        return note_name;
    }

    public String getNote_text() {
        return note_text;
    }

    public String getNote_time() {
        return note_time;
    }
}
